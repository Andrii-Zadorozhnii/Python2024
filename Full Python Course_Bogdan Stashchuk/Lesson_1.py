# Main Types

# String -> str "Andrii"

print('Andrii')

# Integer -> int 10

print(2+2)
print(10)

# Boolean -> bool True

print(True)
print(False)

# List -> list[1, 2, 3] (have only values)

print([1, 'abc', True])

# Vocabulary -> dict{'min': 5, 'max':8} (have key and value)

print({'min': 5, 'max':8})
